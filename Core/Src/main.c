/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  uint32_t WorkList = ALL_EVENTS;
  uint32_t last_chk_time	= 0;
  uint32_t last_sim_update  = 0;
  uint32_t last_app_update  = 0;
  uint32_t rest_until_blck	= 0; //Restzeit auf die nächsten 100ms (jitter ausgleich!)
  uint8_t  led_500ms_count	= 0;
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART1_UART_Init();
  MX_UART4_Init();
  MX_USART2_Init();

  /* USER CODE BEGIN 2 */
  memset(&SysConfig, 0, sizeof(SysConfig));
  memset(&SysState, 0, sizeof(SysState));

  MB_Init(&huart2);       // initialisierung der Modbus
  SIM_Init(&huart1);      //UART-Handlers und der Simulator
  TERM_Init(&huart4);     //UART-Handlers für den Terminal

  FSM_Init();
  /* USER CODE END 2 */


  SysConfig.MaxFloors = 3;

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
	  //Abarbeiten der Tasks:
	  if (WorkList & MODBUS_EVENT)
	  {
		  ResetEvent(MODBUS_EVENT); //Zurücksetzen, um neue Events während abarbeitung zu ermöglichen
		  MB_Process();    			// Modbus Master Engine
	  }
	  if (WorkList & SIMUL_EVENT)
	  {
		  ParseSimulation();
		  SIM_Process();
		  ResetEvent(SIMUL_EVENT); //Zurücksetzen, um neue Events während abarbeitung zu ermöglichen
	  }
	  if (WorkList & DEBUG_EVENT)
	  {
		  ParseCommand();
		  TERM_Process();
		  ResetEvent(DEBUG_EVENT); 	//Zurücksetzen, um neue Events während abarbeitung zu ermöglichen
	  }
	  if (WorkList & APP_EVENT)
	  {
		  //Alive LED
		  if (led_500ms_count++ >= LEDCHECK_BLK)
		  {
			  GPIO_PinState LED;
			  LED = HAL_GPIO_ReadPin(GPIOB, LED2_Pin);
			  HAL_GPIO_WritePin(GPIOB, LED2_Pin, LED==GPIO_PIN_SET?GPIO_PIN_RESET:GPIO_PIN_SET);
			  led_500ms_count = 0;
	      }

		  //APP
		  FSM_Cycle_100ms();

		  ResetEvent(APP_EVENT); 	//Zurücksetzen, um neue Events während abarbeitung zu ermöglichen
	  }

	  //Time and Jitter handling
	  last_chk_time = HAL_GetTick();

	  //Time Trigger for Simulation
      if (last_chk_time - last_sim_update >= SIMCHECK_BLK * TIMEBLOCK_MS) {  // hier wird die applikation jede 500ms geupdate
    	  SetEvent(SIMUL_EVENT);
    	  SetEvent(DEBUG_EVENT);
          last_sim_update = last_chk_time;
      }

      //trigger für die APP
      if (last_chk_time - last_app_update >= APPCHECK_BLK * TIMEBLOCK_MS) {  // hier wird die applikation jede 500ms geupdate
    	  SetEvent(APP_EVENT);
          last_app_update = last_chk_time;
      }

	  //More to do? Yes => Work! NO => Wait
	  WorkList = GetEvent();
	  if (!WorkList)
	  {
		  rest_until_blck = TIMEBLOCK_MS - (last_chk_time % TIMEBLOCK_MS);
		  if (rest_until_blck == 0 || rest_until_blck > TIMEBLOCK_MS) rest_until_blck = TIMEBLOCK_MS;
		  WorkList = LowPower_Delay(rest_until_blck);   // HW Delay (ausführung im 100ms raster o. event-gesteuert)
	  }
    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

